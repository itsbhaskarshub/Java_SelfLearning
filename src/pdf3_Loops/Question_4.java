package pdf3_Loops;
/*
 * 4. Write a program to print the odd and even numbers.
 */

public class Question_4 {
	
	public static void main(String[] args) {
		
		for(int i = 0; i<=10; i++) {
			
				if (i % 2 == 0) {
					System.out.println("even");
				}
				else {
					System.out.println("odd");
				}
			}
		}
		

	}


